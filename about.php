                                </div>
                                <div class="card-body">
                                    <p>The <b>Data and Evidence for Justice Reform (DE JURE)</b> program is a collaboration between the World Bank’s Governance Global Practice and the Development Impact Evaluation (DIME) team. By leveraging the Bank's relationship with governments, the DE JURE program aims to harness the potential of recent changes in data availability to establish a global data infrastructure for the justice sector, and – through rigorous analysis and experimentation – expand the evidence base on the economics of justice reform.</p>

                                    <p>The program has three objectives:

                                    <p><b>I. DATA:</b> Work with World Bank operations and client governments to strengthen case management and administrative data systems, and pilot a public data depository that brings together key elements from these systems in the form of a series of ‘Doing Justice’ indicators;</p>

                                    <p><b>II. MEASUREMENT:</b> Use administrative and survey data, in conjunction with economic theory and literature, to develop an empirically-validated measurement framework that lays the foundation for research on the economics of justice reform;</p>

                                    <p><b>III. LEARNING:</b> Work with client governments to tackle priority policy research questions to understand the impacts of changes in laws and regulations, information and monitoring systems, and incentives and enforcement mechanisms, by embedding experimental research into the rollout and scale-up of justice sector interventions.</p>
                                    
